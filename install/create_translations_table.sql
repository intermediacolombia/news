-- Tabla para almacenar traducciones del sistema
CREATE TABLE IF NOT EXISTS system_translations (
    id INT AUTO_INCREMENT PRIMARY KEY,
    lang_code VARCHAR(10) NOT NULL DEFAULT 'es',
    trans_key VARCHAR(255) NOT NULL,
    trans_value TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    UNIQUE KEY unique_key_lang (trans_key, lang_code),
    INDEX idx_lang (lang_code),
    INDEX idx_key (trans_key)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

